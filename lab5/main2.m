clc;

x = [-1.5, -1.2, -0.9, -0.6, -0.3, 0, 0.3, 0.6, 0.9, 1.2, 1.5]';
y = [0.0829, 0.2192, 0.5794, 1.5315, 4.0481, 10.7, 4.0481, 1.5315, 0.5794, 0.2192, 0.0829]';

% Замена: X = |x|, Y = ln(y)
X = abs(x);
Y = log(y);


coeffs = polyfit(X, Y, 1);
A = coeffs(1);
B = coeffs(2);

b = A;
a = exp(B);

fprintf('y = %f * exp(%f * |x|)\n', a, b);






