% Создаем вектор значений x на интервале, например, от 0.1 до 3
x = 0.5:0.01:3;
% Вычисляем значения функции f(x)
f = sqrt(x + 1) - 1 ./ x;
% Построение графика
plot(x, f, 'LineWidth', 2); % Рисуем график функции
grid on; % Включаем сетку
hold on; % Позволяем добавить на этот же график другие линии
plot(x, zeros(size(x)), 'k--'); % Рисуем ось X (y=0) черным пунктиром
title('График функции f(x) = sqrt(x+1) - 1/x');
xlabel('x');
ylabel('f(x)');



